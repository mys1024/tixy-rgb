export function bindUsefulVariables(obj: any) {
  obj.PI = Math.PI
  obj.E = Math.E
  obj.sin = Math.sin
  obj.cos = Math.cos
  obj.tan = Math.tan
  obj.exp = Math.exp
  obj.pow = Math.pow
  obj.sqrt = Math.sqrt
  obj.log = Math.log
  obj.abs = Math.abs
  obj.floor = Math.floor
  obj.ceil = Math.ceil
  obj.round = Math.round
  obj.random = Math.random
}
